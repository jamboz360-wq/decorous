/* ============================================================
   Language engine
   ============================================================ */

const I18N = (() => {
  const STORAGE_KEY = 'dp_lang';
  let current = localStorage.getItem(STORAGE_KEY) || 'en';

  function get(key) {
    const t = TRANSLATIONS[current];
    return t[key] !== undefined ? t[key] : (TRANSLATIONS.en[key] || '');
  }

  function apply() {
    const t = TRANSLATIONS[current];
    const html = document.documentElement;
    html.setAttribute('lang', t.lang);
    html.setAttribute('dir', t.dir);
    document.body.setAttribute('dir', t.dir);

    // Update all [data-i18n] elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = get(key);
      if (val !== undefined) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = val;
        } else {
          el.innerHTML = val;
        }
      }
    });

    // Update select options with [data-i18n-opts]
    document.querySelectorAll('[data-i18n-opts]').forEach(sel => {
      const key = sel.getAttribute('data-i18n-opts');
      const opts = get(key);
      if (Array.isArray(opts)) {
        const first = sel.querySelector('option[value=""]');
        sel.innerHTML = '';
        if (first) sel.appendChild(first);
        opts.forEach(o => {
          const opt = document.createElement('option');
          opt.textContent = o;
          sel.appendChild(opt);
        });
      }
    });

    // RTL-specific toggle adjustments
    document.querySelectorAll('.lang-toggle').forEach(btn => {
      btn.setAttribute('data-current', current);
    });

    // Persist
    localStorage.setItem(STORAGE_KEY, current);
  }

  function toggle() {
    current = current === 'en' ? 'ar' : 'en';
    apply();
    // Re-run page-specific JS hooks if available
    if (typeof onLangChange === 'function') onLangChange(current);
    // Dispatch custom event so any module can listen
    document.dispatchEvent(new CustomEvent('langchange', { detail: { lang: current } }));
  }

  function init() {
    apply();
  }

  return { get, toggle, apply, init, current: () => current };
})();

/* Auto-init on DOM ready */
document.addEventListener('DOMContentLoaded', () => I18N.init());
